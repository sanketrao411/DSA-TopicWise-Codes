#include <bits/stdc++.h>
using namespace std;

int main()
{

    int num = 5;
    switch (num)
    {
    case 1:
        cout << "Value : 1" << endl;
        break;

    case 3:
        cout << "Value : 3" << endl;
        break;
    case 5:
        cout << "Value : 5" << endl;
        break;
    default:
        break;
    }
    return 0;
}